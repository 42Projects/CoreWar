#include "cw_common.h"

uint32_t	swap_uint32(uint32_t d)
{
	return ((d & 0x000000ff) << 24
			| (d & 0x0000ff00) << 8
			| (d & 0x00ff0000) >> 8
			| (d & 0xff000000) >> 24);
}
